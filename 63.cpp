class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int> >& obstacleGrid) {
        int row = obstacleGrid.size();
        int col = obstacleGrid[0].size();
        vector<int> dp(col, 1);
        for (int i = col - 1; i >= 0; --i) {
            if (obstacleGrid[row - 1][i] == 1) {
                for (int j = i; j >= 0; --j) {
                    dp[j] = 0;
                }
                break;
            }
        }
        row--;
        while (row--) {
            for (int i = col - 1; i >= 0; --i) {
                if (obstacleGrid[row][i] == 1) {
                    dp[i] = 0;
                } else {
                    if (i == col - 1) {
                        if (dp[i] != 0)
                            dp[i] = 1;
                    } else {
                        dp[i] += dp[i + 1];
                    }
                }
            }
        }
        return dp[0];
    }
};
